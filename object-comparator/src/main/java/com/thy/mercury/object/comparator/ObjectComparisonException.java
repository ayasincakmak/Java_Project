package com.thy.mercury.object.comparator;

public class ObjectComparisonException extends Exception
{
    public ObjectComparisonException(String message)
    {
        super(message);
    }

    public ObjectComparisonException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public ObjectComparisonException()
    {
        super();
    }

    public ObjectComparisonException(Throwable cause)
    {
        super(cause);
    }
}
