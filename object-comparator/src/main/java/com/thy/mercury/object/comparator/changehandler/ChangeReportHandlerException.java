package com.thy.mercury.object.comparator.changehandler;

public class ChangeReportHandlerException extends Exception
{
    public ChangeReportHandlerException()
    {
        super();
    }
    public ChangeReportHandlerException(String message, Throwable cause)
    {
        super(message, cause);
    }
    public ChangeReportHandlerException(String message)
    {
        super(message);
    }
    public ChangeReportHandlerException(Throwable cause)
    {
        super(cause);
    }
}
